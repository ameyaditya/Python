print(20 - 10 / 5 * 3 ** 2)
# the first four operators are known to us, the '**' operators is the exponent operation
# 2 ** 3 means 2 to the power of 3
# the order of execution or precedence of operators is as follows
#exponential
#division and multiplication (whichever appears first is executed)
#addition and substraction (whichever appears first is executed)
# the output is always of float type
print((20 - 10) / (5 * 3) ** 2)
#order of precendence is changed using parenthesis  
