#using if condition
def ifcondition():
    a = 5
    if a<5:
        print("less than 5")
    else:
        print("equal or greater than 5")
#the arguments for the if condition can be put in the parenthesis or just written as it is
#to create a long if loop we use elif
def ifcondition():
    a = 10
    if a<5:
        print("less than 5")
    elif a == 5:
        print("equal to 5")
    else:
        print("greater than 5")
#the above function is a example of multiline if condition
